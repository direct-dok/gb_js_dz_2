function addition(a, b) {
    return a + b;
}

function subtraction(a, b) {
    return a - b;
}

function multiplication(a, b) {
    return a * b;
}

function division(a, b) {
    return a / b;
}

console.log('addition:', addition(4,5));
console.log('subtraction:', subtraction(4,5));
console.log('multiplication:', multiplication(4,5));
console.log('division:', division(4,5));